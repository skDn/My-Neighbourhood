package com.myneighbourhood.utils;

import android.graphics.Bitmap;

public class User {
    private static int ids = 0;

    private long id;
    private String username;
    private String password;
    private String firstName;
    private String lastName;
    private Bitmap image;
    private String phone;
    private String email;

    private Rating rating;
    private Address address;

    public User(String username, String firstName, String lastName, String password, String email, String phone, Bitmap image) {
        this.username = username;
        this.firstName = firstName;
        this.lastName = lastName;
        this.password = password;
        this.email = email;
        this.phone = phone;
        this.image = image;
    }

    // from db
    public User(long id, String username, String firstName, String lastName, String password, String email, String phone, Bitmap image) {
        this.id = id;
        this.username = username != null ? username : "";
        this.firstName = firstName != null ? firstName : "";
        this.lastName = lastName != null ? lastName : "";
        this.password = password != null ? password : "";
        this.email = email != null ? password : "";
        this.phone = phone != null ? password : "";
        this.image = image;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public Bitmap getImage() {
        return image;
    }

    public void setImage(Bitmap image) {
        this.image = image;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public Rating getRating() {
        return rating;
    }

    public void setRating(Rating rating) {
        this.rating = rating;
    }

    public Address getAddress() {
        return address;
    }

    public void setAddress(Address address) {
        this.address = address;
    }

    @Override
    public String toString() {
        return "User: " + username;
    }
}
